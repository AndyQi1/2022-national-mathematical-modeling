%%土壤板结化权重因子计算
disp('请输入判断矩阵A')
A=[1,3,4;1/3,1,2;1/4,1/2,1];
[n,n] = size(A);

Sum_A = sum(A);   %sum函数默认是对矩阵的每一列进行累加，即按行求和
SUM_A = repmat(Sum_A,n,1);    %将Sum_A这个行向量，重复n行，重复一列
Stand_A = A ./ SUM_A;         %将矩阵A归一化，即每一个元素除以其所在列的和

disp('算术平均法求权重的结果为：');
disp(sum(Stand_A,2)./n)      %把归一化的矩阵的每一行累加，然后除以n,得到权重
